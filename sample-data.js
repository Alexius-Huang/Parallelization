const COLOR_CODES_KEYS = Object.keys(COLOR_CODES);
const COLOR_CODES_DETAIL = [50, 100, 200, 300, 400, 500, 600, 700, 800, 900];

const randomColor = () => {
  const randomCode = COLOR_CODES_KEYS[Math.floor(Math.random() * COLOR_CODES_KEYS.length)];
  const randomDetail = COLOR_CODES_DETAIL[Math.floor(Math.random() * COLOR_CODES_DETAIL.length)];
  return COLOR_CODES[randomCode][randomDetail];
};

const boardIds = [-1, 1, 2, 3];
const randomBoardId = () => boardIds[Math.floor(Math.random() * boardIds.length)];
const boardState = [0, 1, 2];
const randomBoardState = () => boardState[Math.floor(Math.random() * boardState.length)];

const now = Date.now();
const anHour = 60 * 60 * 1000;
const aDay = 24 * anHour;
const aWeek = 7 * aDay;
const aMonth = 30 * aDay;
const anHourAgo = now - anHour;
const aDayAgo = now - aDay;
const aWeekAgo = now - aWeek;
const aMonthAgo = now - aMonth;
const times = [anHourAgo, aDayAgo, aWeekAgo, aMonthAgo];
const randomTime = () => times[Math.floor(Math.random() * times.length)];

module.exports = () => {
  const data = {
    epics: [],
    tickets: [],
    boards: [],
    messages: [],
    statistics: {
      total: {
        tickets: 0,
        epics: 0,
        boards: 0,
      },
    },
  };

  for (let i = 1; i <= 3; i += 1) {
    data.boards.push({
      id: i,
      title: `Board - ${i}`,
      columns: ['TODO', 'On Progress', 'Completed'],
      description: 'Lorem Ipsum',
      color: randomColor(),
      createdAt: aMonthAgo,
    });
    data.statistics.total.boards += 1;
  }

  for (let i = 1; i <= 5; i += 1) {
    data.epics.push({
      id: i,
      title: `Epic - ${i}`,
      description: `This is Epic - ${i}`,
      color: randomColor(),
      createdAt: aMonthAgo,
    });
    data.statistics.total.epics += 1;

    for (let j = 1; j <= 5; j += 1) {
      const boardId = randomBoardId();
      data.tickets.push({
        epicId: i,
        id: ((i - 1) * 5) + j,
        boardId,
        boardState: boardId === -1 ? -1 : randomBoardState(),
        point: 1,
        title: 'Sample Ticket',
        description: 'Description of sample ticket',
        createdAt: randomTime(),
      });
      data.statistics.total.tickets += 1;
    }
  }

  return data;
};
